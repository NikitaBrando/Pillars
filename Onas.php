<p class="onastext">Национальный парк учрежден 17 апреля 2019 года Распоряжением Правительства РФ №763-р «О создании ФГБУ «Национальный парк «Ленские Столбы»» в целях сохранения природных комплексов и объектов в долинах рек Лены и Буотамы (правый приток реки Лены), имеющих большое природоохранное, научное и рекреационное значение, уникальных геологических объектов общероссийского и мирового значения – Ленских и Буотамских столбов, на базе Природного парка «Ленские Столбы», созданного в 1995-м году.<br><br>

