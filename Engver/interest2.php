The research activities of the Nature Park are aimed at developing and implementing scientific methods for preserving the biological diversity of natural and historical-cultural complexes and objects in conditions of recreational and economic use, long-term monitoring of the dynamics of natural processes, assessment and forecast of the ecological situation in the region, reproduction and rational use of natural resources.
<br>
Research activities in the Nature Park are carried out by:
<br>
- Research institutions and higher educational institutions of the relevant profile on a contractual basis according to the programs agreed with the Ministry of Nature Protection of the Republic of Sakha (Yakutia) in common with the Natural Park.
<br>
A biological station or other scientific unit may be established within the boundaries of a Natural Park to conduct research in the field of nature conservation, the direction of which should be coordinated with the management of the Natural Park.
<br>
Plans of biotechnical, recultivation, restoration and forestry works, as well as measures to regulate the number of wild animals in the territory of the Natural Park are considered without fail at meetings of the Scientific and Technical Council.
<br>
A lot of work on the description and assessment of the significance of historical, cultural and natural heritage objects in the park was carried out at the design stage when the park was created in 1992-1994.

