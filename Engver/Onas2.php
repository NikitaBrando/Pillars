		<h2>LENA PILLARS NATIONAL PARK</h2>
<p class="onastext">Lena Pillars National Park is a unique place, the main and most recognizable attraction of Yakutia, included in the UNESCO Natural Heritage List.</p>
<img src="imagge/og_og_1563947972234183802.jpg" class="cartinki">

<p class="onastext">The National Park was established on April 17, 2019 by the Decree of the Government of the Russian Federation No. 763-r "On the creation of the Federal State Budgetary Institution "Lena Pillars National Park" in order to preserve natural complexes and objects in the valleys of the Lena and Buotama rivers (right tributary of the Lena River) of great environmental, scientific and recreational importance, unique geological objects of all-Russian and of world importance – Lena and Buotam Pillars, on the basis of the Lena Pillars Nature Park, created in 1995.<br><br>

There are many unique and interesting places on the territory of the park: the famous rock formations "Lena Pillars", which represent numerous remnants of the destruction of bedrock. The length of the rocks is more than 40 km, and the height in places reaches up to 220 meters. From afar, the pillars seem like a majestic monolithic wall, descending steeply into the river. Also in the park you can see bison included in the Red Book of the International Union for Conservation of Nature in the forest bison nursery "Ust-Buotama". The park is also known for the Buotama River, where fossils (lagerstettes) dating back to the Early Cambrian period (about 542 million years ago) were found, and an exotic landscape for central Yakutia – sand dunes (tukulans) "Kysyl Elesin", "Saamys-Kumaga", an archaeological monument, a site of ancient man "Deering-Yuryakh", dated about 370 thousand years ago, rock paintings, geological and cultural monuments.<br><br>

The Lena Pillars National Park is rightfully considered the "pearl" of the Republic of Sakha (Yakutia) and the starting point, or the beginning of an exciting and informative journey through the vast expanses of Yakutia.</p>
